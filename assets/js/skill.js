function showgd() {
    document.getElementById("showgd").style.display = "block";
    document.getElementById("hidegd").style.display = "none";
    document.getElementById("gd").classList.add("show");
}

function hidegd() {
    document.getElementById("hidegd").style.display = "block";
    document.getElementById("showgd").style.display = "none";
    document.getElementById("gd").classList.remove("show");
}

